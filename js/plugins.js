window.requestFullScreen = function(el){
	
	var f = false;
	
	if(el.webkitRequestFullScreen != undefined){
		f = el.webkitRequestFullScreen;
	}
	else if(el.mozRequestFullScreen != undefined){
		f = el.mozRequestFullScreen;
	}
	
	if(f){
		f.apply(el, arguments);
	}
	else{
		return false;
	}
	
};